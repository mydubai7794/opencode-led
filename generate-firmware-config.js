import fs from "fs";
import path from "path";
import os from "os";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const configPaths = [
  path.join(__dirname, "mqtt-config.json"),
  path.join(os.homedir(), ".config/opencode/plugins/mqtt-config.json"),
];

function findJson(paths) {
  for (const p of paths) {
    try {
      return JSON.parse(fs.readFileSync(p, "utf-8"));
    } catch {}
  }
  return null;
}

const config = findJson(configPaths);
const outPath = path.join(__dirname, "firmware/ai-led-firmware/firmware-config.h");

if (!config || !config.wifi) {
  console.log("mqtt-config.json not found or missing wifi section, generating empty header (AP mode)");
  const emptyHeader = `// Auto-generated by generate-firmware-config.js - no config found
// Firmware will use EEPROM/AP mode
#define HARDCODED_WIFI_SSID ""
#define HARDCODED_WIFI_PASSWORD ""
#define HARDCODED_BROKER ""
#define HARDCODED_BROKER_PORT 1883
#define HARDCODED_MQTT_USER ""
#define HARDCODED_MQTT_PASS ""
`;
  fs.writeFileSync(outPath, emptyHeader);
  console.log("Written:", outPath);
  process.exit(0);
}

const wifi = config.wifi || {};
const mqtt = config.mode === "remote" ? (config.remote || {}) : (config.local || {});

const header = `// Auto-generated by generate-firmware-config.js
// WiFi: ${wifi.ssid || "(empty)"} | MQTT: ${mqtt.host || "(empty)"}
#define HARDCODED_WIFI_SSID "${wifi.ssid || ""}"
#define HARDCODED_WIFI_PASSWORD "${wifi.password || ""}"
#define HARDCODED_BROKER "${mqtt.host || ""}"
#define HARDCODED_BROKER_PORT ${mqtt.port || 1883}
#define HARDCODED_MQTT_USER "${mqtt.username || ""}"
#define HARDCODED_MQTT_PASS "${mqtt.password || ""}"
`;

fs.writeFileSync(outPath, header);
console.log("Written:", outPath);
console.log("  WiFi SSID:", wifi.ssid || "(empty)");
console.log("  MQTT Broker:", mqtt.host || "(empty)", ":", mqtt.port || 1883);
